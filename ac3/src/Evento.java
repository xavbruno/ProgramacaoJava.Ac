public class Evento {
    String data, nome, local;
    int maxParticipantes;

    Evento() {

    }

    Evento(String data, String nome, String local, int maxParticipantes) {
        this.data = data;
        this.nome = nome;
        this.local = local;
        this.maxParticipantes = maxParticipantes;
    }


}
